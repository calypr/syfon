package main

import (
	"flag"
	"fmt"
	"os"
	"strings"

	"github.com/getkin/kin-openapi/openapi3"
)

func main() {
	in := flag.String("in", "", "input OpenAPI YAML file")
	out := flag.String("out", "", "output *_test.go file")
	pkg := flag.String("pkg", "apigen", "package name for generated test file")
	flag.Parse()

	if *in == "" || *out == "" {
		fmt.Fprintln(os.Stderr, "usage: openapi-testgen -in openapi.yaml -out openapi_generated_test.go [-pkg apigen]")
		os.Exit(2)
	}

	loader := openapi3.NewLoader()
	spec, err := loader.LoadFromFile(*in)
	if err != nil {
		fmt.Fprintf(os.Stderr, "load spec: %v\n", err)
		os.Exit(1)
	}

	var ops []string
	for path, item := range spec.Paths.Map() {
		if item.Get != nil && item.Get.OperationID != "" {
			ops = append(ops, "GET "+path+" -> "+item.Get.OperationID)
		}
		if item.Post != nil && item.Post.OperationID != "" {
			ops = append(ops, "POST "+path+" -> "+item.Post.OperationID)
		}
	}

	content := fmt.Sprintf(`// Code generated by openapi-testgen; DO NOT EDIT.
package %s

import "testing"

func TestOpenAPISpecHasOperations(t *testing.T) {
	ops := []string{%s}
	if len(ops) == 0 {
		t.Fatalf("expected at least one operation in spec")
	}
}
`, *pkg, quoteSlice(ops))

	if err := os.WriteFile(*out, []byte(content), 0o644); err != nil {
		fmt.Fprintf(os.Stderr, "write: %v\n", err)
		os.Exit(1)
	}
}

func quoteSlice(ss []string) string {
	out := make([]string, 0, len(ss))
	for _, s := range ss {
		out = append(out, fmt.Sprintf("%q", s))
	}
	return strings.Join(out, ", ")
}
